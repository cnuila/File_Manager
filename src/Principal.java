        DefaultTableModel modelo = (DefaultTableModel) jTable_modificarRegistro.getModel();
        int rows = modelo.getRowCount(), columns = modelo.getColumnCount();
        Object data[][] = new String[rows][columns];
        boolean flag = true;
        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < columns; j++) {
                if (modelo.getValueAt(i, j) == null) {
                    JOptionPane.showMessageDialog(jd_modificarRegistro, "No pueden haber campos vacÃ­os", "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
                    flag = false;
                    j = columns;
                    i = rows;
                } else {
                    data[i][j] = modelo.getValueAt(i, j);
                }
            }
        }
        if (flag) {
            String registro = "";
            for (int i = 0; i < rows; i++) {
                String llavePrimaria = (String) data[i][0];
                Llave llave = new Llave();
                llave.setLlave(llavePrimaria);
                if (arbolB.search(llave) == null) {
                    JOptionPane.showMessageDialog(jd_modificarRegistro, "No puede modificar la llave primaria", "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
                }
                for (int j = 0; j < columns; j++) {
                    registro += data[i][j];
                    registro += "|";
                }
            }
            int tamanoLlave = llaveActual.getTamano();
            if (tamanoLlave >= registro.length()) {
                try {
                    archivoActual.seek(llaveActual.getOffset());
                    archivoActual.write(registro.getBytes());
                } catch (IOException ex) {
                    Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                
            }
        }

